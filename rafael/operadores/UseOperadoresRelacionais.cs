class UseOperadoresRelacionais 
{
	static void Main() 
	{
		int x = 20;
		int y = 15;

		System.Console.WriteLine(x > y);
		System.Console.WriteLine(x >= y);
		System.Console.WriteLine(x < y);
		System.Console.WriteLine(x <= y);
		System.Console.WriteLine(x == y);
		System.Console.WriteLine(x != y);
	}
}
